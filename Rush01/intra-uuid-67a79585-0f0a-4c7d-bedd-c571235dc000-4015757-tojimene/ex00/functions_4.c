void	save(int i, int j, int k);

void	funcion_4_1(int i, int j, int k)
{
	int	x;

	x = 1;
	if (k == 0)
	{
		while (i < 4)
		{
			save(i, j, x);
			x++;
			i++;
		}
	}
	else if (k == 1)
	{
		while (j < 4)
		{
			save(i, j, x);
			x++;
			j++;
		}
	}
}
