void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	aux;
	int	j;

	i = 0;
	j = size - 1;
	while (i < size / 2)
	{
		aux = tab[i];
		tab[i] = tab[j];
		tab[j] = aux;
		i++;
		j--;
	}
}
